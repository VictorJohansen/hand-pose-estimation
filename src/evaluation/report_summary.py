"""Generate result tables from saved evaluation artifacts.

Run from the project root:
    python -m src.evaluation.report_summary
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Sequence

from src.evaluation.comparison import (
    ARTIFACTS_DIR,
    DEFAULT_COLUMNS,
    LOGS_DIR,
    format_comparison_markdown,
    load_run_summary,
)


PROJECT_ROOT = Path(__file__).resolve().parents[2]
DEFAULT_OUTPUT = PROJECT_ROOT / "reports" / "result-summary.md"


def _metric(summary: dict, key: str) -> float:
    return float(summary["metrics"][key])


def _format_value(value) -> str:
    if isinstance(value, float):
        if value != 0.0 and abs(value) < 0.01:
            return f"{value:.4g}"
        return f"{value:.2f}"
    if isinstance(value, int) and abs(value) >= 1000:
        return f"{value:,}"
    return str(value)


def _format_markdown_table(headers: Sequence[str], rows: Sequence[Sequence]) -> str:
    rendered_rows = [[_format_value(value) for value in row] for row in rows]
    widths = [
        max(len(headers[index]), *(len(row[index]) for row in rendered_rows))
        for index in range(len(headers))
    ]

    def render(row: Sequence[str]) -> str:
        return "| " + " | ".join(
            str(value).ljust(widths[index])
            for index, value in enumerate(row)
        ) + " |"

    return "\n".join(
        [
            render(headers),
            "| " + " | ".join("-" * width for width in widths) + " |",
            *(render(row) for row in rendered_rows),
        ]
    )


def discover_run_names() -> list[str]:
    """Return evaluated run names that also have saved config files."""
    run_names: list[str] = []
    for evaluation_path in sorted(ARTIFACTS_DIR.glob("*/evaluation.json")):
        run_name = evaluation_path.parent.name
        if (LOGS_DIR / run_name / "config.json").exists():
            run_names.append(run_name)
    if not run_names:
        raise FileNotFoundError(
            "No evaluated runs found. Expected artifacts/<run>/evaluation.json "
            "and logs/<run>/config.json."
        )
    return run_names


def _ranking_table(summaries: Sequence[dict]) -> str:
    ranked = sorted(summaries, key=lambda summary: _metric(summary, "mpke_px"))
    rows = [
        (
            rank,
            summary["run_name"],
            _metric(summary, "mpke_px"),
            _metric(summary, "median_sample_mpke_px"),
            _metric(summary, "p95_sample_mpke_px"),
            int(summary["param_count"]),
        )
        for rank, summary in enumerate(ranked, start=1)
    ]
    return _format_markdown_table(
        ("Rank", "Run", "MPKE (px)", "Median (px)", "p95 (px)", "Params"),
        rows,
    )


def _relative_to_best_table(summaries: Sequence[dict]) -> str:
    best = min(summaries, key=lambda summary: _metric(summary, "mpke_px"))
    best_mpke = _metric(best, "mpke_px")
    best_params = int(best["param_count"])
    rows = []
    for summary in sorted(summaries, key=lambda item: item["run_name"]):
        mpke = _metric(summary, "mpke_px")
        params = int(summary["param_count"])
        rows.append(
            (
                summary["run_name"],
                mpke - best_mpke,
                (mpke - best_mpke) / best_mpke * 100.0,
                params - best_params,
                params / best_params,
            )
        )
    return _format_markdown_table(
        (
            "Run",
            "MPKE delta (px)",
            "MPKE delta (%)",
            "Param delta",
            "Param ratio",
        ),
        rows,
    )


def _split_table(summaries: Sequence[dict]) -> str:
    rows = []
    for summary in summaries:
        metrics = summary["metrics"]
        rows.append(
            (
                summary["run_name"],
                int(metrics.get("n_samples", 0)),
                summary["representation"],
                summary.get("epochs", ""),
                summary.get("batch_size", ""),
                summary.get("learning_rate", ""),
            )
        )
    return _format_markdown_table(
        ("Run", "Val samples", "Repr.", "Epochs", "Batch", "LR"),
        rows,
    )


def build_summary_markdown(
    run_names: Sequence[str],
    *,
    command: str = "python -m src.evaluation.report_summary",
) -> str:
    summaries = [load_run_summary(run_name) for run_name in run_names]

    lines = [
        "# Result summary",
        "",
        "Generated by:",
        "",
        "```bash",
        command,
        "```",
        "",
        "## Final comparison",
        "",
        format_comparison_markdown(list(summaries), DEFAULT_COLUMNS),
        "",
        "## Ranked by MPKE",
        "",
        _ranking_table(summaries),
        "",
        "## Relative to best MPKE",
        "",
        _relative_to_best_table(summaries),
        "",
        "## Run metadata",
        "",
        _split_table(summaries),
        "",
    ]
    return "\n".join(lines)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Generate result tables from saved evaluation artifacts.",
    )
    parser.add_argument(
        "runs",
        nargs="*",
        help="Run names to include. Defaults to all evaluated runs.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help="Markdown file to write.",
    )
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    run_names = args.runs or discover_run_names()
    command = "python -m src.evaluation.report_summary"
    if args.runs:
        command = f"{command} {' '.join(args.runs)}"
    output = args.output
    if not output.is_absolute():
        output = PROJECT_ROOT / output
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(build_summary_markdown(run_names, command=command))
    try:
        print(output.relative_to(PROJECT_ROOT))
    except ValueError:
        print(output)


if __name__ == "__main__":
    main()
